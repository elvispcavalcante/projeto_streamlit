class Venda:
    def __init__(self, data_venda, valor_total_venda, logradouro, numero, complemento, bairro, municipio, latitude,
                 longitude, nome_entregador, cpf, id_entregador, id_loja, nome_cliente, id_cliente):
        self.data_venda = data_venda
        self.valor_total_venda = valor_total_venda
        self.logradouro = logradouro
        self.numero = numero
        self.complemento = complemento
        self.bairro = bairro
        self.municipio = municipio
        self.latitude = latitude
        self.longitude = longitude
        self.nome_entregador = nome_entregador
        self.cpf = cpf
        self.id_entregador = id_entregador
        self.id_loja = id_loja
        self.nome_cliente = nome_cliente
        self.id_cliente = id_cliente
